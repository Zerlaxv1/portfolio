<div class="project-description">
	<h2>Contexte</h2>
	<p>
		Mini-projet PHP monolithique realise dans le cadre de la ressource R3.01. L'objectif
		etait de choisir un sport et de construire une application web pour aider a la gestion
		d'une equipe amateur.
	</p>

	<h2>Objectif</h2>
	<p>
		Proposer une application simple pour organiser les joueurs, les matchs et les
		entrainements, avec une interface serveur classique et des operations CRUD completes.
	</p>

	<h2>Points techniques</h2>
	<ul>
		<li>Architecture monolithique en PHP</li>
		<li>Base de donnees MySQL pour la persistance</li>
		<li>Pages HTML generees cote serveur</li>
		<li>Gestion des utilisateurs et authentification simple</li>
		<li>Tableau de bord avec resume des statistiques</li>
	</ul>

	<h2>Apprentissages</h2>
	<p>
		Ce projet m'a fait travailler la conception fonctionnelle, la gestion des donnees et
		les bonnes pratiques de developpement web de base (structure, validation, securisation
		des entrees).
	</p>
</div>

