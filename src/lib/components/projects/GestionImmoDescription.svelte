<div class="project-description">
	<h2>Contexte</h2>
	<p>
		Projet universitaire réalisé en équipe dans le cadre du BUT Informatique (S3.A01). 
		L'objectif était de concevoir et développer une application de bureau complète pour la 
		gestion d'un portefeuille immobilier.
	</p>

	<h2>Objectifs</h2>
	<p>
		Créer une application permettant de gérer efficacement les biens immobiliers, les locataires, 
		les contrats de location et les paiements. L'application devait offrir une interface graphique 
		intuitive et garantir la persistance des données.
	</p>

	<h2>Réalisations techniques</h2>
	<ul>
		<li>Conception d'une architecture MVC pour structurer l'application</li>
		<li>Développement d'interfaces graphiques avec Java Swing</li>
		<li>Implémentation d'une base de données SQLite pour la persistance</li>
		<li>Mise en place de tests unitaires avec JUnit 4</li>
		<li>Gestion des CRUD (Create, Read, Update, Delete) pour toutes les entités</li>
		<li>Validation des données saisies et gestion des erreurs</li>
	</ul>

	<h2>Compétences développées</h2>
	<p>
		Ce projet m'a permis de consolider mes compétences en programmation orientée objet, 
		en conception de bases de données relationnelles et en développement d'interfaces utilisateur. 
		J'ai également appris à travailler en équipe et à respecter les bonnes pratiques de développement 
		(tests, documentation).
	</p>
</div>

