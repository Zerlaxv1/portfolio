<div class="project-description">
	<h2>Contexte</h2>
	<p>
		Mini-projet PHP monolithique réalisé dans le cadre de la ressource R3.01. L'objectif
		était de choisir un sport et de construire une application web pour aider à la gestion
		d'une équipe amateur.
	</p>

	<h2>Objectif</h2>
	<p>
		Proposer une application simple pour organiser les joueurs, les matchs et les
		entrainements, avec une interface serveur classique et des opérations CRUD complètes.
	</p>

	<h2>Points techniques</h2>
	<ul>
		<li>Architecture monolithique en PHP</li>
		<li>Base de données MySQL pour la persistance</li>
		<li>Pages HTML générées entièrement côté serveur (sans JavaScript)</li>
		<li>Gestion des utilisateurs et authentification simple</li>
		<li>Tableau de bord avec résumé des statistiques</li>
	</ul>

	<h2>Apprentissages</h2>
	<p>
		Ce projet m'a fait travailler la conception fonctionnelle, la gestion des données et
		les bonnes pratiques de développement web de base (structure, validation, sécurisation
		des entrées).
	</p>
</div>

